<h3> ACTIVIDAD # 2 EJERCICIOS </h3>
<p>OBJETIVOS </p>
<p>✓ Hacer un proceso de análisis (entrada -- proceso --salida) para cada problema </p>
<p>✓ Manejar PSeInt como pseudolenguaje para generar alternativas de solución a 
problemas o situaciones planteados. </p>
<p>✓ Identificar las representaciones de un algoritmo en Pseudocódigo y diagrama de 
flujo. </p>
EJERCICIOS
<p>1. Diseñe un algoritmo que permita obtener el porcentaje de estudiantes que hacen
parte de cada uno de los siguientes cursos: </p>
<p>• Introducción a la Tecnología en Informática </p>
<p>• Programación Básica </p>
<p>• Electiva CPC </p>
<p>2. Simular por medio de un algoritmo la siguiente situación: </p>
<p>1. Una persona se dirige a un cajero electrónico </p>
<p>2. Inserta su tarjeta  </p>
<p>3. El cajero muestra el menú de opciones </p>
<p>4. El usuario elige la opción de consultar saldo </p>
<p>5. El cajero le solicita la clave de acceso </p>
<p>6. El cajero muestra el saldo que tiene el usuario </p>
<p>7. El usuario vuelve e inserta la tarjeta </p>
<p>8. Se repite el paso 3 </p>
<p>9. El usuario elige retirar una cantidad de dinero </p>
<p>10. El cajero le solicita la clave de acceso </p>
<p>11. El cajero actualiza el saldo del usuario </p>
<p>Nota: utiliza estos pasos para plantear una solución utilizando estructuras 
secuenciales.</p>

<p>3. Realiza un algoritmo que solicite 3 números enteros, los multiplique, el resultado de
la multiplicación lo sume con el segundo número ingresado, el resultado de la suma
lo divida por el resultado de la resta entre el primero y tercer numero ingresado. Al
final debe mostrar los resultados de cada una de las operaciones realizadas.</p>
<p>4. Diseñe una solución en algoritmo para la siguiente situación: Una persona debe
hacer un pequeño mercado para la semana:</p>
<p>• Leche</p>
<p>• Carnes</p>
<p>• Cereales</p>
<p>• Frutas</p>
<p>• Verduras</p>
<p>Del anterior listado se debe solicitar el nombre del producto, la cantidad de unidades
a comprar y se debe establecer un precio unitario. El supermercado ese día tiene
un descuento del 15% aplicado al valor total de la compra. Al final el algoritmo debe
mostrar una especie de factura de compra.</p>
<p>5. Algoritmo: El estudiante del programa de Tecnología en Informática desea saber
cual es su calificación teniendo en cuenta lo siguiente:</p>
  <p>a. Primer corte 35%</p>
  <p>b. Segundo corte 35%</p>
  <p>c. Tercer corte 30%</p>


